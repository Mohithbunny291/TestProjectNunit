namespace OurNunitLib
{
    public class Calc
    {
        public int Add(int x, int y)
        {
            return x + y;
        }
        public int Avg(int x, int y)
        {
            return (x + y) / 2;
        }
        public int Diff(int x, int y)
        {
            return x - y;
        }
        public int Multi(int x, int y)
        {
            return x * y;
        }
    }
}
